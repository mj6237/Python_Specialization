'''
1. Create a tuple: colors = ("red", "green", "blue"). 
2. Check if "green" is in the colors tuple. Print "Green is in the tuple." or "Green is NOT in the tuple." 
accordingly. 
3. Check if "yellow" is in the colors tuple. Print "Yellow is in the tuple." or "Yellow is NOT in the tuple." 
accordingly. 
'''
colors = ("red", "green", "blue")

if "green" in colors :
    print("Green is in the tuple.")
else :
    print("Green is NOT in tuple.")
if "yellow" in colors :
    print("Yellow is in the tuple.")
else :
    print("Yellow is NOT in tuple.")