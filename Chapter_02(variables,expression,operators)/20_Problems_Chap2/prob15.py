# Take a five-digit number as input and print the sum of its digits.

dig1 = int(input("Enter first digit :"))
dig2 = int(input("Enter second digit :"))
dig3 = int(input("Enter third digit :"))
dig4 = int(input("Enter fourth digit :"))
dig5 = int(input("Enter fifth digit :"))
sum = dig1 + dig2 + dig3 + dig4 + dig5
print(f"Sum is : {sum}")